slic = ["Mango", "Apple", "Litchi", "Orange", "Peach"]
for slice1 in slic[0:3]:
    print(f"The first three items in the list are: {slice1}" )

for slice2 in slic[2:]:
    print(f"Three tiems from the middle of the list are: {slice2}")

for slice3 in slic[-3:]:
    print(f"The last three items in the list are: {slice3}")